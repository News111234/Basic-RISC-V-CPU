// ================================================
// 模块: wb_top
// 功能: 写回阶段顶层模块
// 描述: 流水线的最后一级，负责将结果写回寄存器堆
// 注意: 写回阶段主要是组合逻辑，不包含流水线寄存器
// ================================================
`timescale 1ns/1ps  // 时间单位：1纳秒，精度1皮秒

module wb_top (
    // ========== 时钟和复位端口 ==========
    input  wire        clk_i,      // 时钟信号
    // 用于同步调试输出和可能的性能计数器
    
    input  wire        rst_n_i,    // 复位信号（低电平有效）
    // 复位时清零内部计数器
    
    // ========== 来自访存阶段的数据输入 ==========
    input  wire [31:0] alu_result_i,   // ALU计算结果
    // 来自访存阶段传递过来的ALU运算结果
    
    input  wire [31:0] mem_rdata_i,    // 内存读取数据
    // 来自数据存储器的读取数据（加载指令的结果）
    
    input  wire [31:0] pc_plus4_i,     // PC+4值（用于跳转指令）
    // 跳转指令的返回地址
    
    // 新增：CSR结果输入
    input  wire [31:0] csr_result_i,     // 来自CSR模块的结果

    // ========== 来自访存阶段的控制信号 ==========
    input  wire [1:0]  wb_sel_i,       // 写回选择信号
    // 决定写回数据的来源：00=ALU, 01=内存, 10=PC+4, 11=CSR
    
    input  wire        reg_we_i,       // 寄存器写使能信号
    // 1: 需要写寄存器，0: 不需要写寄存器
    // 注意：store指令和branch指令不写寄存器，所以reg_we_i=0
    
    input  wire [4:0]  rd_addr_i,      // 目标寄存器地址
    // 5位地址，指定要写入哪个寄存器（x0-x31）
    
    // ========== 输出到寄存器堆 ==========
    output wire [31:0] wb_data_o,      // 输出：写回寄存器的数据
    // 连接到寄存器堆的写数据端口
    
    output wire        reg_we_o,       // 输出：寄存器写使能
    // 连接到寄存器堆的写使能端口
    
    output wire [4:0]  rd_addr_o       // 输出：目标寄存器地址
    // 连接到寄存器堆的写地址端口
    
    // 注意：这三个输出直接连接到regfile的写端口
    // 实际的寄存器写入发生在下一个时钟上升沿
);

// ========== 内部信号定义 ==========
wire [31:0] wb_data;      // 从wb_mux输出的写回数据
// 这个信号连接写回数据选择器的输出

// ========== 实例化写回数据选择器 ==========
wb_mux u_wb_mux (
    // 数据输入
    .alu_result_i (alu_result_i),  // 连接输入的ALU结果
    .mem_rdata_i  (mem_rdata_i),   // 连接输入的内存读取数据
    .pc_plus4_i   (pc_plus4_i),    // 连接输入的PC+4值
    .csr_data_i   (csr_result_i),         // CSR数据暂时接地（预留功能）
    // CSR（控制和状态寄存器）功能将在后续添加
    
    // 控制信号
    .wb_sel_i     (wb_sel_i),      // 连接输入的写回选择信号
    
    // 输出
    .wb_data_o    (wb_data)        // 输出到内部信号wb_data
    // 这是经过选择后的写回数据
);

// ========== 输出信号连接 ==========
assign wb_data_o = wb_data;      // 将内部数据连接到输出端口
// 这个信号将送到寄存器堆的wdata_i端口

assign reg_we_o = reg_we_i;      // 写使能信号直通
// 写回阶段不修改写使能信号，直接传递

assign rd_addr_o = rd_addr_i;    // 目标寄存器地址直通
// 写回阶段不修改目标地址，直接传递


// ========== 性能计数器（可选功能） ==========
// 统计写回操作的次数，用于性能分析
reg [31:0] write_count = 0;  // 32位写操作计数器

// 计数器更新逻辑
always @(posedge clk_i or negedge rst_n_i) begin
    if (!rst_n_i) begin
        // 复位时清零计数器
        write_count <= 32'b0;
    end
    else if (reg_we_i && (rd_addr_i != 5'b0)) begin
        // 当需要写寄存器且目标不是x0时，计数器加1
        write_count <= write_count + 1;
    end
end

// ========== 写回阶段特点说明 ==========
// 1. 主要是组合逻辑：数据选择器是组合逻辑
// 2. 不包含流水线寄存器：写回阶段通常不添加额外的延迟
// 3. 实际写入时机：数据在下一个时钟上升沿写入寄存器堆
// 4. 关键作用：根据指令类型选择正确的数据写回寄存器

// ========== 写回阶段的数据流向 ==========
// 输入（来自MEM阶段） -> wb_mux（选择） -> 输出（到regfile）
// 控制信号直接传递，不进行修改

endmodule